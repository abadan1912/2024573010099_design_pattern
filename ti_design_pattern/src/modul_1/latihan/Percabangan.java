package modul_1.latihan;

public class Percabangan {
    public static void main(String[] args) {

        int bilangan = 10;

        if (bilangan % 2 == 0) {
            System.out.println(bilangan + " adalah bilangan Genap");
        } else {
            System.out.println(bilangan + " adalah bilangan Ganjil");
        }
    }
}
